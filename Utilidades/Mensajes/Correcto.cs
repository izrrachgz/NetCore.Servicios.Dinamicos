namespace Utilidades.Mensajes
{
  /// <summary>
  /// Provee mensajes que indican acciones y operaciones efectuadas
  /// correctamente
  /// </summary>
  public static class Correcto
  {
    /// <summary>
    /// Usado cuando la operación solicitada ha concluido
    /// de manera satisfactoria
    /// </summary>
    public const string SolicitudCompletada = @"La solicitud se ha completado correctamente.";

    /// <summary>
    /// Usado cuando el recurso solicitado se ha encontrado correctamente.
    /// </summary>
    public const string RecursoEncontrado = @"Se ha encontrado la informacion solicitada.";

    /// <summary>
    /// Usado cuando el recurso solicitado se ha guardado correctamente a traves de un
    /// mecanismo asociado.
    /// </summary>
    public const string Guardado = @"Se ha guardado correctamente.";
  }
}
